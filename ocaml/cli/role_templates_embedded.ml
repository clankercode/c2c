let lookup role_class =
  try Some (List.assoc role_class templates) with Not_found -> None

let render ~role_class ~alias ~display_name_hint =
  match lookup role_class with
  | None -> None
  | Some body ->
    let alias_re = Str.regexp_string "{alias}" in
    let hint_re  = Str.regexp_string "{display_name_hint}" in
    let body = Str.global_replace alias_re alias body in
    let body = Str.global_replace hint_re  display_name_hint body in
    Some body
